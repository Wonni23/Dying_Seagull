import sys

si = sys.stdin.readline
N = int(si())
arr = [list(map(int, si().split())) for _ in range(N)]
dp1 = [[0] * 3 for _ in range(N)]
dp2 = [[0] * 3 for _ in range(N)]
dp3 = [[0] * 3 for _ in range(N)]
INF = 9999999999
dp1[0][0] = arr[0][0]
dp1[0][1] = INF
dp1[0][2] = INF
dp2[0][0] = INF
dp2[0][1] = arr[0][1]
dp2[0][2] = INF
dp3[0][0] = INF
dp3[0][1] = INF
dp3[0][2] = arr[0][2]
for _ in range(1, N):
    dp1[_][0] = min(dp1[_ - 1][1], dp1[_ - 1][2]) + arr[_][0]
    dp1[_][1] = min(dp1[_ - 1][0], dp1[_ - 1][2]) + arr[_][1]
    dp1[_][2] = min(dp1[_ - 1][0], dp1[_ - 1][1]) + arr[_][2]
    dp2[_][0] = min(dp2[_ - 1][1], dp2[_ - 1][2]) + arr[_][0]
    dp2[_][1] = min(dp2[_ - 1][0], dp2[_ - 1][2]) + arr[_][1]
    dp2[_][2] = min(dp2[_ - 1][0], dp2[_ - 1][1]) + arr[_][2]
    dp3[_][0] = min(dp3[_ - 1][1], dp3[_ - 1][2]) + arr[_][0]
    dp3[_][1] = min(dp3[_ - 1][0], dp3[_ - 1][2]) + arr[_][1]
    dp3[_][2] = min(dp3[_ - 1][0], dp3[_ - 1][1]) + arr[_][2]
print(min(min(dp1[-1][1], dp1[-1][2]), min(dp2[-1][0], dp2[-1][2]), min(dp3[-1][0], dp3[-1][1])))



